//------------------------------------------------------------------------------
// Name: Light()
// Desc: Constructor for the light class
//------------------------------------------------------------------------------
Light::Light()
{
	m_vPosition	= D3DXVECTOR3( 0.0f, 0.0f, 0.0f );
	m_vColour	= D3DXVECTOR3( 0.0f, 0.0f, 0.0f );
}

Light::Light( const D3DXVECTOR3& vPosition, const D3DXVECTOR3& vColour )
{
	m_vPosition	= vPosition;
	m_vColour	= vColour;
}
